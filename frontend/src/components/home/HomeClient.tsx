  function getDeepSections(text: string) {
    const headings = lang === 'FR'
      ? [
          'Ce que la situation est réellement',
          'Ce qui tient le système',
          'Ce qui l’affaiblit',
          'Ce qui pourrait déclencher une escalade',
          'Ce qui pourrait produire une bascule',
          'Ce qu’il faut surveiller maintenant',
        ]
      : [
          'What the situation really is',
          'What holds the system together',
          'What weakens it',
          'What could trigger escalation',
          'What could produce a shift',
          'What to watch now',
        ]
    const normalizedText = headings.reduce((acc, heading) => {
      const escaped = heading.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
      return acc.replace(new RegExp(`\\s*(${escaped})\\s*`, 'gi'), '\n\n$1\n\n')
    }, cleanUiText(text).replace(/\s+([?!.])/g, '$1').replace(/\?\s+[”"]/g, '?”'))

    const paragraphs = normalizedText
      .split(/\n{2,}|\r?\n/)
      .map((part) => part.trim())
      .filter(Boolean)
    if (paragraphs.length === 0) return []

    const normalizedHeadings = headings.map((heading) => heading.toLowerCase())
    const parsedSections: Array<{ heading: string; body: string }> = []
    let current: { heading: string; body: string[] } | null = null

    for (const paragraph of paragraphs) {
      const headingIndex = normalizedHeadings.indexOf(paragraph.toLowerCase())
      if (headingIndex >= 0) {
        if (current && current.body.length > 0) {
          parsedSections.push({ heading: current.heading, body: current.body.join('\n\n') })
        }
        current = { heading: headings[headingIndex], body: [] }
        continue
      }
      if (current) {
        current.body.push(paragraph)
      }
    }

    if (current && current.body.length > 0) {
      parsedSections.push({ heading: current.heading, body: current.body.join('\n\n') })
    }

    if (parsedSections.length > 0) return parsedSections

    return headings.map((heading, index) => ({
      heading,
      body: paragraphs[index] ?? (index === 0 ? paragraphs.join('\n\n') : ''),
    })).filter((section) => section.body)
  }

