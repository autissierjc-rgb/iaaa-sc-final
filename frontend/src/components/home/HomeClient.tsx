function isPersistableSituationCard(sc: Record<string, any> | null | undefined): sc is Record<string, any> {
  if (!sc || typeof sc !== 'object') return false
  const status = String(sc.generation_status ?? 'ok')
  if (status === 'degraded' || status === 'blocked') return false
  return Boolean(
    String(sc.submitted_situation_fr ?? sc.submitted_situation_en ?? '').trim() &&
    String(sc.lecture_systeme_fr ?? sc.lecture_systeme_en ?? '').trim()
  )
}

function isUnavailableLecture(value: string): boolean {
  const text = String(value ?? '').trim()
  return /^(?:lecture syst[eè]me indisponible pour cette carte|system reading unavailable for this card)\.?$/i.test(text)
}

