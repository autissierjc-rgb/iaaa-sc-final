type CompassMode = 'idle' | 'light' | 'full' | 'off'

function Boussole({ mode, onClick, disabled }: {
  mode: CompassMode; onClick: () => void; disabled: boolean
}) {
  const angle = useRef(0)
  const [needle, setNeedle] = useState(0)
  const raf = useRef<number | null>(null)

  useEffect(() => {
    const spinning = mode === 'full' || mode === 'light'
    if (spinning) {
      const speed = mode === 'full' ? 4 : 2
      const spin = () => {
        angle.current = (angle.current + speed) % 360
        setNeedle(angle.current)
        raf.current = requestAnimationFrame(spin)
      }
      raf.current = requestAnimationFrame(spin)
    } else {
      if (raf.current) cancelAnimationFrame(raf.current)
      setNeedle(0)
    }
    return () => { if (raf.current) cancelAnimationFrame(raf.current) }
  }, [mode])

  const active = mode !== 'off' && mode !== 'idle'
  const pulse  = mode === 'idle' && !disabled

  return (
    <button
      onClick={onClick}
      disabled={disabled || mode === 'off'}
      title="Analyser la situation"
      style={{
        width: 52, height: 52, borderRadius: '50%',
        border: 'none', background: 'none', padding: 0,
        cursor: disabled ? 'not-allowed' : 'pointer',
        filter: pulse ? 'drop-shadow(0 0 8px rgba(184,154,106,0.7))' : 'none',
        transition: 'filter 0.3s',
      }}
    >
      <svg width="52" height="52" viewBox="0 0 52 52">
        <circle cx="26" cy="26" r="25" fill="none"
          stroke={pulse || active ? GOLD : '#C8C4BC'}
          strokeWidth="1.5" strokeDasharray={pulse || active ? '3 2' : '2 2'}
          opacity={pulse || active ? 1 : 0.4}
        />
        <circle cx="26" cy="26" r="22"
          fill={pulse || active ? NAVY : '#B0BAC8'}
          opacity={pulse || active ? 1 : 0.5}
        />
        {Array.from({ length: 32 }).map((_, i) => {
          const a = (i * 360 / 32) * Math.PI / 180
          const r1 = i % 4 === 0 ? 16 : 18
          return (
            <line key={i}
              x1={26 + r1 * Math.sin(a)} y1={26 - r1 * Math.cos(a)}
              x2={26 + 20 * Math.sin(a)} y2={26 - 20 * Math.cos(a)}
              stroke={GOLD} strokeWidth={i % 4 === 0 ? 1 : 0.5}
              opacity={pulse || active ? 0.6 : 0.2}
            />
          )
        })}
        <g transform={`rotate(${needle} 26 26)`}>
          <polygon points="26,9 24,26 28,26"
            fill="#fff" opacity={pulse || active ? 0.95 : 0.4} />
          <polygon points="26,43 24,26 28,26"
            fill={GOLD_L} opacity={pulse || active ? 0.9 : 0.3} />
        </g>
        <circle cx="26" cy="26" r="3"
          fill={pulse || active ? GOLD_L : '#9AABB8'} />
        <circle cx="26" cy="26" r="1.5" fill="#fff" />
        {pulse && (
          <circle cx="26" cy="26" r="22" fill="none" stroke={GOLD} strokeWidth="1" opacity="0.4">
            <animate attributeName="r"       values="22;26;22" dur="2s" repeatCount="indefinite" />
            <animate attributeName="opacity" values="0.4;0;0.4" dur="2s" repeatCount="indefinite" />
          </circle>
        )}
      </svg>
    </button>
  )
}

// ── AstrolabeRadial ───────────────────────────────────────────────────────────
function AstrolabeRadial({ scores }: {
  scores: Array<{ display_score: number; name: string; branch: string }>
}) {
  const cx = 120, cy = 126
  const RINGS   = [32, 62, 92]
  const LABEL_R = 118
  const FILLS   = ['#B8D4F0', '#F0CA70', '#E87C7C']
  const STROKES = ['#7AAEDC', '#D4A830', '#C84040']
  const EMPTY   = '#E0DCD4'
  const EMPTY_S = '#C8C4BC'

  return (
    <svg viewBox="0 0 240 294" width="226" height="276" style={{ overflow: 'visible' }}>
      <circle cx={cx} cy={cy} r={108} fill="none" stroke="#C8B880" strokeWidth="1.2" />
      {Array.from({ length: 32 }).map((_, t) => {
        const ta = (t * 360 / 32 - 90) * Math.PI / 180
        const isMaj = t % 4 === 0
        const r1 = 100, r2 = isMaj ? 94 : 97
        return (
          <line key={t}
            x1={cx + r1 * Math.cos(ta)} y1={cy + r1 * Math.sin(ta)}
            x2={cx + r2 * Math.cos(ta)} y2={cy + r2 * Math.sin(ta)}
            stroke="#C8B880" strokeWidth={isMaj ? 1 : 0.5} />
        )
      })}
      {RINGS.map(r => (
        <circle key={r} cx={cx} cy={cy} r={r}
          fill="none" stroke="#DDD8CC" strokeWidth="0.6" strokeDasharray="3 3" />
      ))}
      {scores.map((s, i) => {
        const a = (i * 45 - 90) * Math.PI / 180
        const perpA = a + Math.PI / 2
        const xEnd = cx + 95 * Math.cos(a)
        const yEnd = cy + 95 * Math.sin(a)
        const lx = cx + LABEL_R * Math.cos(a)
        const ly = cy + LABEL_R * Math.sin(a)
        const dotColor = s.display_score === 3 ? '#801050' : s.display_score === 2 ? '#888000' : '#004858'
        return (
          <g key={i}>
            <line x1={cx} y1={cy} x2={xEnd} y2={yEnd} stroke="#E0DCD4" strokeWidth="0.8" />
            {RINGS.map((r, lvl) => {
              const px = cx + r * Math.cos(a)
              const py = cy + r * Math.sin(a)
              const hl = r * 0.28
              const hw = r * 0.12
              const filled = s.display_score >= (lvl + 1)
              const t1x = px + hl * Math.cos(a),  t1y = py + hl * Math.sin(a)
              const t2x = px - hl * Math.cos(a),  t2y = py - hl * Math.sin(a)
              const s1x = px + hw * Math.cos(perpA), s1y = py + hw * Math.sin(perpA)
              const s2x = px - hw * Math.cos(perpA), s2y = py - hw * Math.sin(perpA)
              return (
                <polygon key={lvl}
                  points={`${t1x},${t1y} ${s1x},${s1y} ${t2x},${t2y} ${s2x},${s2y}`}
                  fill={filled ? FILLS[lvl] : EMPTY}
                  stroke={filled ? STROKES[lvl] : EMPTY_S}
                  strokeWidth={filled ? '1' : '0.5'}
                  strokeLinejoin="miter"
                  opacity={filled ? 0.92 : 0.5}
                />
              )
            })}
            <circle cx={lx} cy={ly} r="11" fill="#F8F3E8" stroke="#C8B880" strokeWidth="0.9" />
            <circle cx={lx} cy={ly - 5} r="2" fill={dotColor} opacity="0.85" />
            <text x={lx} y={ly + 3.5} textAnchor="middle" dominantBaseline="middle"
              fontSize="8" fontFamily="'Cinzel',Georgia,serif" fill="#6A5A38">
              {s.branch}
            </text>
          </g>
        )
      })}
      <circle cx={cx} cy={cy} r="7" fill="#D4BC78" stroke="#A89050" strokeWidth="1" />
      <circle cx={cx} cy={cy} r="3" fill="#8A6830" />
      {[
        { c: '#B8D4F0', l: 'Calme' },
        { c: '#F0CA70', l: 'Modéré' },
        { c: '#E87C7C', l: 'Dominant' },
      ].map((item, i) => (
        <g key={i} transform={`translate(${16 + i * 74}, 268)`}>
          <polygon points="8,0 4,8 8,16 12,8" fill={item.c} stroke="none" opacity="0.9" />
          <text x="18" y="11" fontSize="10" fill={TXT3} fontFamily="'Cinzel',Georgia,serif">
            {item.l}
          </text>
        </g>
      ))}
    </svg>
  )
}

