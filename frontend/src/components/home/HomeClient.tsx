function sanitizeSituationDraft(value: string): string {
  const markers = [
    /\n\s*Restreint\s*\n\s*Public/i,
    /\n\s*Private\s*\n\s*Public/i,
    /\n\s*POWERED BY IAAA\+/i,
    /\n\s*SITUATION SOUMISE/i,
    /\n\s*SUBMITTED SITUATION/i,
    /\n\s*Lecture structurelle/i,
    /\n\s*Structural reading/i,
  ]
  const cuts = markers
    .map((pattern) => value.search(pattern))
    .filter((index) => index > 0)
  const cut = cuts.length > 0 ? Math.min(...cuts) : -1
  return (cut > 0 ? value.slice(0, cut) : value)
    .replace(/\n{3,}/g, '\n\n')
    .trimStart()
}

