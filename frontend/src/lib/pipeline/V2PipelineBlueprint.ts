export function pipelineTotals(blueprint: V2PipelineBlueprint) {
  const total_latency_budget_ms = blueprint.stages.reduce((total, stage) => total + stage.latency_budget_ms, 0)
  const blocking_stages = blueprint.stages.filter((stage) => stage.blocks_generation).length
  const wired_stages = blueprint.stages.filter((stage) => stage.status === 'wired').length

  return {
    total_latency_budget_ms,
    blocking_stages,
    wired_stages,
    stage_count: blueprint.stages.length,
  }
}
